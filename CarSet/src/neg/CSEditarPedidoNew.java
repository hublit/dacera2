    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        int confirmado = JOptionPane.showConfirmDialog(this,"¿Estas seguro que quieres eliminar el pedido?");

        if (JOptionPane.OK_OPTION == confirmado)
        {
            int pr_id = Integer.parseInt(jTextNumero.getText());
            //String query="DELETE from pr_proveedores where pr_id="+pr_id;
            String query = "UPDATE pe_pedidos SET pe_estado='Inactivo' where pe_num='"+pr_id+"'";

            boolean rs = CSDesktop.datos.manipuladorDatos(query);
             if(rs)
             {
                    jButtonModificar.setEnabled(false);
                    JLabel errorFields = new JLabel("<HTML><FONT COLOR = Blue>Se ha producido un error al guardar en la base de datos</FONT></HTML>");
                    JOptionPane.showMessageDialog(null,errorFields);
                    jButtonModificar.setEnabled(true);
            }
            else
            {
                try {
                    jButtonModificar.setEnabled(false);
                    JLabel mensaje = new JLabel("<HTML><FONT COLOR = Blue>Los datos se han borrado correctamente.</FONT></HTML>");
                    JOptionPane.showMessageDialog(null, mensaje);
                    jButtonModificar.setEnabled(true);
                    CSDesktop.EditarPedidoNew.dispose();
                    //CSDesktop.ResultPedidoNew.dispose();
                    CSDesktop.menuBuscarPedidoNew.setEnabled(true);
                    CSResultBuscarPedido resultBuscarPedido = new CSResultBuscarPedido(consulta);
                } catch (UnknownHostException ex) {
                    Logger.getLogger(CSEditarPedidoNew.class.getName()).log(Level.SEVERE, null, ex);
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(CSEditarPedidoNew.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(CSEditarPedidoNew.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

