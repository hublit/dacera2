    /**
     * Insertar los servicios especiales del Proveedor desde el Proveedor de plantilla
     * @param proveedor
     */
    public int buscarServiciosProveedor(int idPr) throws SQLException
    {
        int exist = 0;

        String query = "SELECT count(1) as cont FROM sr_servicios WHERE pr_id = '"+idPr+"'";

        System.out.println(query);
        ResultSet rsExist = CSDesktop.datos.select(query);
        try {
            rsExist.next();
        } catch (SQLException ex) {
            Logger.getLogger(CSServicioProveedorNew.class.getName()).log(Level.SEVERE, null, ex);
        }
        exist = rsExist.getInt("cont");
        rsExist.close();

        return exist;
    }
