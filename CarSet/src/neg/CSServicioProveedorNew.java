    public CSServicioProveedorNew(String query, int proveedor) throws SQLException
    {
        ResultSet rs = CSDesktop.datos.select(query);

        CSDesktop.EditarProveedorNew.setEnabled(false);
        initComponents();
        limitacionesCampos();
        pr_id = proveedor;
        int numeroFila = 0;

        try{
            while (rs.next()){
                jTextTurismo.setText(rs.getString("sr_turismo"));
                jTextFLM.setText(rs.getString("sr_flm"));
                jTex4x4.setText(rs.getString("sr_todoterreno"));
                jTextFurgoneta.setText(rs.getString("sr_furgoneta"));
                jTextFurgon.setText(rs.getString("sr_furgon"));
                jTextSuv.setText(rs.getString("sr_suv"));
                jTextEspecial.setText(rs.getString("sr_especial"));
                jTextDias.setText(rs.getString("sr_dias"));
                numeroFila++;
            }
            rs.close();
        } 
        catch (SQLException ex)
        {
            Logger.getLogger(CSResultBuscarProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
        CSDesktop.ServicioProveedor = new JInternalFrame("Servicios Especiales de Proveedor", true, false, false, true );
        CSDesktop.ServicioProveedor.getContentPane().add( this, BorderLayout.CENTER );
        CSDesktop.ServicioProveedor.pack();
        CSDesktop.ServicioProveedor.dispose();
        CSDesktop.elEscritorio.add( CSDesktop.ServicioProveedor );
        Dimension pantalla = CSDesktop.elEscritorio.getSize();
        Dimension ventana = CSDesktop.ServicioProveedor.getSize();
        CSDesktop.ServicioProveedor.setLocation(
           (pantalla.width - ventana.width) / 2,
           (pantalla.height - ventana.height) / 2);
        CSDesktop.ServicioProveedor.setVisible( true );
         
        KeyListener l = new KeyListener()
        {
            public void keyTyped(KeyEvent e) {}
            public void keyPressed(KeyEvent e)
            {
               if (e.getKeyCode() == KeyEvent.VK_ENTER)
               {
                    jButtonModificar.doClick();
                }
                if (e.getKeyCode() == KeyEvent.VK_ESCAPE)
                {
                    jButtonCancelar.doClick();
                }
            }
            public void keyReleased(KeyEvent e) {}
        };
        
        addKeyListener(l);
    }

