    public CSSelectMailClienteFactura(String query,BeanCorreoCliente mail,String nombre)
    {
        mailF=mail;
        nombreF=nombre;
        
        ResultSet rs = CSDesktop.datos.select(query);

        modelo.setColumnIdentifiers(new String[] {"NOMBRE", "EMAIL"});

        int numeroFila = 0;
        try {
            int z=0;
            while (rs.next()) {
                Object[] datosFila = new Object[modelo.getColumnCount()];
                int j = 0;
                for (int k = 0; k < 2; k++)
                {
                    if (k==0)
                    {
                        datosFila[j] = rs.getObject(k + 1);
//                        System.out.println("Dato" + k + " " + rs.getObject(k + 1));
                        nombresMail.put(z,rs.getObject(k + 1));
                        j++;
                    }
                    else if ( k == 1)
                    {
                        datosFila[j] = rs.getObject(k + 1);
//                        System.out.println("Dato" + k + " " + rs.getObject(k + 1));
                        datosMail.put(z,rs.getObject(k + 1));
                        j++;
                    }
                }
                modelo.addRow(datosFila);
                numeroFila++;
                z++;
            }
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(CSResultBuscarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

        initComponents();
        jTable1.setModel(modelo);
        TableColumn columna = jTable1.getColumnModel().getColumn(0);
        columna.setPreferredWidth(200);
        TableColumn columna1 = jTable1.getColumnModel().getColumn(1);
        columna1.setPreferredWidth(300);

    }

